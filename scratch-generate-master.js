// B. 엑셀 파일에서 공모 사업 데이터 파싱하여 추가
// ==========================================
const excelPath = path.join(__dirname, '2026 여주 경기공유학교 학교맞춤형 학교별 예산 배정 내역(3차_교육청 예산).xlsx');

try {
  const workbook = xlsx.readFile(excelPath);
  const sheetName = '2026  배정 내역(누적_교육청)';
  const sheet = workbook.Sheets[sheetName];
  
  // 헤더 정보를 포함하여 2차원 배열로 파싱
  const rows = xlsx.utils.sheet_to_json(sheet, { header: 1 });
  
  // 프로젝트 컬럼 정보 추출 (Row 4에 사업 코드 및 명칭 위치)
  // 인덱스 3부터 8까지 프로젝트 목록 존재
  const projectColumns = [];
  const row4 = rows[3] || [];
  for (let col = 3; col <= 8; col++) {
    if (row4[col]) {
      projectColumns.push({
        index: col,
        name: String(row4[col]).trim()
      });
    }
  }

  console.log(`Excel sheet '${sheetName}'에서 찾은 공모 사업 컬럼:`, projectColumns);

  // 데이터 파싱 (Row 5부터 시작)
  let excelDataCount = 0;
  for (let i = 4; i < rows.length; i++) {
    const row = rows[i];
    if (!row || row.length === 0) continue;
    
    const rawSchoolName = row[1]; // B열: 학교명
    if (!rawSchoolName) continue;
    
    const schoolNameStr = String(rawSchoolName).trim();
    
    // 유치원은 데이터베이스에 계정이 존재하지 않으므로 스킵
    if (schoolNameStr.endsWith("유") || schoolNameStr.endsWith("유치원")) {
      continue;
    }
    
    const normalizedSchool = normalizeSchoolName(schoolNameStr);
    
    // 각 공모 사업별 예산 금액 확인
    projectColumns.forEach((project) => {
      const rawAmt = row[project.index];
      if (rawAmt !== undefined && rawAmt !== null) {
        const amt = parseInt(String(rawAmt).replace(/[^0-9-]/g, ""), 10);
        if (!isNaN(amt) && amt > 0) {
          masterRows.push({
            "학교명": normalizedSchool,
            "사업유형": "공모",
            "세부사업코드_및_명칭": project.name,
            "재원구분": "교육청 지원금",
            "교부금액": amt
          });
          excelDataCount++;
        }
      }
    });
  }
  
  console.log(`Excel에서 파싱 성공한 공모 예산 행 개수: ${excelDataCount}건`);
} catch (err) {
  console.error("Excel 파일 파싱 오류:", err.message);
}

