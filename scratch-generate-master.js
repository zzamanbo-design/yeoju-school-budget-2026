function normalizeSchoolName(name) {
  if (!name) return "";
  name = name.trim().replace(/\s+/g, "");
  
  if (nameMap[name]) return nameMap[name];
  
  if (name.endsWith("초")) return name + "등학교";
  if (name.endsWith("중")) return name + "학교";
  if (name.endsWith("고")) return name + "학교";
  
  return name;
}

